    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String sql = "insert into vehicle values(?, ?, ?, ?, ?, ?, ?, default)";
        try{
            pst = conn.prepareStatement(sql);
            pst.setString(1, jTextField1.getText());
            pst.setString(2, (String) jComboBox1.getSelectedItem());
            pst.setString(3, WordUtils.capitalizeFully(jTextField2.getText()));
            pst.setString(4, WordUtils.capitalizeFully(jTextField3.getText()));
            pst.setString(5, jTextField4.getText());
            pst.setString(6, jTextField5.getText());
            pst.setString(7, jTextField6.getText());
            pst.execute();
            pst.close();
            JOptionPane.showMessageDialog(null, "New Car Successfully Added");
            setVisible(false);
            Home ob = new Home();
            ob.setVisible(true);
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

